import { writeFileSync, mkdirSync, existsSync } from 'fs';
import { join } from 'path';
import chalk from 'chalk';
import { AgentSpec, HarnessFiles } from '../types/schemas.js';

interface GenerateOptions {
  spec: AgentSpec;
  outputDir: string;
  force?: boolean;
}

/**
 * Generates a complete agent workspace from an agent spec.
 * 
 * This is the core of the framework - it takes a high-level agent description
 * and produces all the files needed to run and evaluate that agent.
 */
export async function generateHarness(options: GenerateOptions): Promise<string> {
  const { spec, outputDir, force = false } = options;
  
  // Create workspace directory
  const workspacePath = join(outputDir, spec.agentId);
  
  if (existsSync(workspacePath) && !force) {
    throw new Error(`Workspace already exists at ${workspacePath}. Use --force to overwrite.`);
  }
  
  console.log(chalk.dim(`   Creating workspace: ${workspacePath}`));
  mkdirSync(workspacePath, { recursive: true });
  
  // Generate all files
  const files = generateAllFiles(spec);
  
  // Write harness files
  const harnessDir = join(workspacePath, 'harness');
  mkdirSync(harnessDir, { recursive: true });
  
  writeFileSync(join(harnessDir, 'SOUL.md'), files.soul);
  writeFileSync(join(harnessDir, 'AGENTS.md'), files.agents);
  writeFileSync(join(harnessDir, 'MEMORY.md'), files.memory);
  writeFileSync(join(harnessDir, 'LEARNINGS.md'), files.learnings);
  
  // Write tests
  const testsDir = join(workspacePath, 'tests');
  mkdirSync(testsDir, { recursive: true });
  writeFileSync(join(testsDir, 'eval-suite.json'), files.tests);
  
  // Write runtime adapter
  const runtimeDir = join(workspacePath, 'runtime');
  mkdirSync(runtimeDir, { recursive: true });
  writeFileSync(join(runtimeDir, 'adapter.js'), files.runtimeAdapter);
  
  // Write spec copy for reference
  writeFileSync(join(workspacePath, 'spec.json'), JSON.stringify(spec, null, 2));
  
  console.log(chalk.dim(`   ✓ Written: harness/SOUL.md`));
  console.log(chalk.dim(`   ✓ Written: harness/AGENTS.md`));
  console.log(chalk.dim(`   ✓ Written: harness/MEMORY.md`));
  console.log(chalk.dim(`   ✓ Written: harness/LEARNINGS.md`));
  console.log(chalk.dim(`   ✓ Written: tests/eval-suite.json`));
  console.log(chalk.dim(`   ✓ Written: runtime/adapter.js`));
  console.log(chalk.dim(`   ✓ Written: spec.json`));
  
  return workspacePath;
}

function generateAllFiles(spec: AgentSpec): HarnessFiles {
  return {
    soul: generateSoulMd(spec),
    agents: generateAgentsMd(spec),
    memory: generateMemoryMd(spec),
    learnings: generateLearningsMd(spec),
    tests: generateTestSuite(spec),
    runtimeAdapter: generateRuntimeAdapter(spec),
  };
}


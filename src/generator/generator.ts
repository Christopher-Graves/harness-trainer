function generateTestSuite(spec: AgentSpec): string {
  const tests = generateDefaultTests(spec);
  
  return JSON.stringify({
    version: '1.0',
    agentId: spec.agentId,
    description: `Evaluation suite for ${spec.agentId}`,
    tests,
  }, null, 2);
}

function generateDefaultTests(spec: AgentSpec): any[] {
  // Generate sensible default tests based on agent responsibilities
  return spec.responsibilities.map((resp, i) => ({
    name: `test-${i + 1}-${slugify(resp.split(' ')[0])}`,
    description: `Test: ${resp}`,
    task: `Perform the following responsibility: ${resp}`,
    expectedOutput: `Agent should complete the responsibility successfully and document the process.`,
    successCriteria: [
      'Task completed end-to-end',
      'Progress documented in MEMORY.md',
      'Learnings appended to LEARNINGS.md',
      'No errors or unhandled exceptions',
    ],
  }));
}


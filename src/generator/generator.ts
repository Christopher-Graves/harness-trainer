function getVoiceForRole(role: string): string {
  const roleLower = role.toLowerCase();
  if (roleLower.includes('research')) return 'Analytical, curious, thorough. Cite sources.';
  if (roleLower.includes('engineer') || roleLower.includes('dev')) return 'Technical, precise, pragmatic. Code-first.';
  if (roleLower.includes('writer') || roleLower.includes('content')) return 'Creative, engaging, clear. Voice matters.';
  if (roleLower.includes('coach') || roleLower.includes('lifecoach')) return 'Supportive, direct, action-oriented. Push growth.';
  if (roleLower.includes('financial') || roleLower.includes('tax')) return 'Conservative, detail-oriented, compliant. Numbers first.';
  if (roleLower.includes('architect')) return 'Strategic, opinionated, well-researched. Design patterns matter.';
  return 'Professional, helpful, efficient. Get the job done.';
}

function generateToolPattern(tool: any): string {
  return `### ${tool.name}
**When to use:** [context where this tool is appropriate]
**How to use:** 
\`\`\`
// Example usage
\`\`\`
**Common pitfalls:** [what goes wrong]
**Error handling:** [how to recover]`;
}

function slugify(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');
}
